int	ft_is_prime(int nb)
{
	int a;

	a = (nb % 3);
	if (nb >= 0)
	{
		if (nb == 1)
			return (0);
		if (nb == 3 || nb == 2 || nb == 5 || nb == 7)
			return (1);
		if (a == 0 || (nb % 2) == 0 || (nb % 5) == 0 || (nb % 7) == 0)
			return (0);
		else
			return (1);
	}
	return (0);
}

char	*ft_strstr(char *str, char *to_find)
{
	int o;
	int p;

	o = 0;
	while (str[o] != '\0')
	{
		p = 0;
		while (str[o] == to_find[p] && str[o] != '\0' && to_find[p] != '\0')
		{
			o++;
			p++;
		}
		if (to_find[p] == '\0')
			return (str + o - p);
		else
			o = o - p;
		o++;
	}
	return (0);
}

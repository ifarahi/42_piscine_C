int		ft_str_is_uppercase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= ('a' - 32) && str[i] <= ('z' - 32)))
		{
			return (0);
		}
		else
		{
			i++;
		}
	}
	return (1);
}
